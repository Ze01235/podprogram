def calculate_rectangle_area(width, length):
    area = width * length
    return area


width1 = 5
length1 = 10
area1 = calculate_rectangle_area(width1, length1)
print(f"Площадь прямоугольника с шириной {width1} и длиной {length1} равна {area1}")

width2 = 7
length2 = 12
area2 = calculate_rectangle_area(width2, length2)
print(f"Площадь прямоугольника с шириной {width2} и длиной {length2} равна {area2}")
